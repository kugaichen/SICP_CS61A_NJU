def twenty_twenty_three():
    """Come up with the most creative expression that evaluates to 2023,
    using only numbers and the +, *, and - operators.

    Expected result:
    >>> twenty_twenty_three()
    2023
    """
    return 2023


def sum(a, b):
    """Compute the sum of a and b.

    Expected result:
    >>> sum(1, 2)
    3
    >>> sum(3, 8)
    11
    """
    return a+b


def diff_square(a, b):
    """Compute the difference of square a and square b.

    Expected result:
    >>> diff_square(3, 2)
    5
    >>> diff_square(3, 4)
    -7
    """
    return a*a - b*b
